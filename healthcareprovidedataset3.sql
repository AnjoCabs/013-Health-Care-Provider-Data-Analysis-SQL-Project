-- PATIENTS VOLUME AND GROWTH ANALYSIS
-- 1. How many patients visit the hospital each month?
SELECT
	YEAR(dateOfVisit) AS `YEAR`,
    MONTH(dateOfVisit) AS `month`,
    COUNT(*) AS patientCount
FROM visits
GROUP BY YEAR(dateOfVisit), MONTH(dateOfVisit)
ORDER BY YEAR(dateOfVisit), MONTH(dateOfVisit);

-- 2. Which departments receive the most patient visits?
SELECT 
	v.departmentId,
    d.department,
    COUNT(visitId) AS totalPatientCount
FROM visits v
JOIN departments d
	ON v.departmentId	= d.departmentId
GROUP BY
	v.departmentId,
    d.department
ORDER BY v.departmentId;

-- 3. Which cities generate the most patients?
SELECT
    c.city,
    COUNT(patientId) AS totalPatientCount
FROM patients p
JOIN cities c
	ON p.cityId	= c.cityId
GROUP BY 
    c.city
ORDER BY totalPatientCount DESC;

-- REVENUE AND FINANCIAL PERFORMANCE
-- 4. What is the total revenue generated by each department?
SELECT
	department,
    SUM(COALESCE(v.treatmentcost, 0) + COALESCE(v.medicationCost, 0)) AS totalCosts
FROM visits v
JOIN departments d
	ON v.departmentid = d.departmentId
GROUP BY department
ORDER BY totalCosts DESC;

-- 5. Which procedures generate the highest revenue?
SELECT
    p.procedure,
    SUM(COALESCE(v.treatmentcost, 0) + COALESCE(v.medicationCost, 0)) AS totalCosts
FROM visits v
JOIN procedures p
    ON v.procedureId = p.procedureId 
GROUP BY p.procedure;

-- 6. What percentage of revenue is covered by insurance?
SELECT
    ROUND(
        SUM(insuranceCoverage) /SUM(treatmentCost + medicationCost + roomCharges) * 100,2) AS insuranceCoveragePercentage
FROM visits;

-- 7. Which insurance providers contribute the most revenue?
SELECT
	v.insuranceId,
    SUM(treatmentCost + medicationCost + roomCharges) AS totalRevenue
FROM visits v
JOIN insurance i
	ON 	v.insuranceId = i.insuranceId
GROUP BY v.insuranceId
ORDER BY totalRevenue DESC;


-- PATIENT SATISFACTION ANALYSIS
-- 8. Which departments have the highest patient satisfaction scores?
SELECT
	v.departmentId,
	d.department,
    AVG(patientSatisfactionScore) AS AVGSatisfactionScore
FROM visits v
