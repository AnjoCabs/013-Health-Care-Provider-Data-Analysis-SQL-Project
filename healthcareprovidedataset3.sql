	v.providerId,
	p.providerName,
    AVG(patientSatisfactionScore) AS AVGSatisfactionScore
FROM visits v
JOIN providers p
	ON p.providerId = v.providerId
GROUP BY
	v.providerId,
	p.providerName
ORDER BY AVGSatisfactionScore DESC;


-- 10. Does treatment cost affect patient satisfaction?
SELECT
    CASE
        WHEN treatmentCost < 500 THEN 'Low Cost'
        WHEN treatmentCost BETWEEN 500 AND 999.99 THEN 'Medium Cost'
        ELSE 'High Cost'
    END AS costCategory,
    COUNT(*) AS totalVisits,
    ROUND(AVG(patientSatisfactionScore), 2) AS avgSatisfactionScore
FROM visits
GROUP BY costCategory
ORDER BY avgSatisfactionScore DESC;

