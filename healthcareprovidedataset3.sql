-- EMERGENCY SERVICE ANALYSIS
-- 15. What percentage of visits are emergency visits?
SELECT
    ROUND(
        SUM(CASE WHEN referralSource = 'Emergency' THEN 1 ELSE 0 END) * 100.0 / NULLIF(COUNT(*), 0), 2 ) AS percentageEmergencyVisit
FROM visits;

