-- 21. Which age groups generate the highest healthcare spending?
SELECT
    CASE
        WHEN p.age BETWEEN 0 AND 17 THEN 'Children (0-17)'
        WHEN p.age BETWEEN 18 AND 35 THEN 'Young Adults (18-35)'
        WHEN p.age BETWEEN 36 AND 55 THEN 'Middle-Aged (36-55)'
        WHEN p.age BETWEEN 56 AND 75 THEN 'Seniors (56-75)'
        ELSE 'Elderly (76+)'
    END AS ageGroup,
    COUNT(DISTINCT v.patientId) AS total_patients,
    ROUND(SUM(v.treatmentCost + v.medicationCost + v.roomCharges),2) AS totalHealthcareSpending,
    ROUND(AVG(v.treatmentCost + v.medicationCost + v.roomCharges),2) AS avgSpendingPerVisit
FROM visits v
JOIN patients p
    ON v.patientId = p.patientId
GROUP BY ageGroup
ORDER BY totalHealthcareSpending DESC;

-- 22. Which gender has the highest average treatment cost?
