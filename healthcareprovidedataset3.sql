-- 18. Which insurance providers have the highest unpaid claims?
SELECT
    v.insuranceId,
    i.insuranceProvider,
    SUM(COALESCE(v.insuranceCoverage, 0)) AS unpaidClaims
FROM visits v
JOIN insurance i
    ON v.insuranceId = i.insuranceId
WHERE v.paymentStatus = 'Pending' 
GROUP BY
    v.insuranceId,
    i.insuranceProvider
ORDER BY unpaidClaims DESC; 

-- 19. Which referral sources generate the most patients?
SELECT
    referralSource,
    COUNT(DISTINCT patientId) AS totalPatients,
    ROUND(COUNT(DISTINCT patientId) * 100.0 /
        (SELECT COUNT(DISTINCT patientId)
            FROM visits),2) AS patientPercentage
FROM visits
GROUP BY referralSource
ORDER BY totalPatients DESC;

-- 20. Which referral sources generate the highest revenue?
