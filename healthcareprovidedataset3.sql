FROM visits v
JOIN patients p
	ON v.patientId = p.patientId
GROUP BY gender
ORDER BY avgtotalCost DESC;

-- 23. What percentage of visits have unpaid balances?
SELECT
    COUNT(*) AS totalVisits,
    COUNT(CASE WHEN LOWER(paymentStatus) IN ('unpaid','pending','partially paid')
            THEN 1 END) AS unpaidVisits,
    ROUND(COUNT(CASE WHEN LOWER(paymentStatus) IN ('unpaid','pending','partially paid')
			THEN 1 END) * 100.0 / COUNT(*),2) AS unpaidBalancePercentage
FROM visits;

-- 24. Which diagnoses have high costs but low patient satisfaction?
