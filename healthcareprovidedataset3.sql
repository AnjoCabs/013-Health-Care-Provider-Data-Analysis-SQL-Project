-- 25. Which departments have both high patient volume and low satisfaction?
SELECT
    d.department,
    COUNT(v.visitId) AS total_visits,
    ROUND(AVG(v.patientSatisfactionScore),2) AS avgSatisfaction
FROM visits v
JOIN departments d
    ON v.departmentId = d.departmentId
GROUP BY d.department
HAVING
    COUNT(v.visitId) >
    (SELECT AVG(dept_visits)
	FROM (
            SELECT COUNT(*) AS dept_visits
            FROM visits
            GROUP BY departmentId) x    )
AND AVG(v.patientSatisfactionScore)< (SELECT AVG(patientSatisfactionScore)
        FROM visits)
ORDER BY total_visits DESC;


